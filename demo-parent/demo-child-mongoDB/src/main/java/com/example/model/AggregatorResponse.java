package com.example.model;

public class AggregatorResponse {


	private int maxUID;
	private int minUID;
	private int maxUIDCount;
	private int minUIDCount;
	
	
	
	public int getMaxUIDCount() {
		return maxUIDCount;
	}
	public void setMaxUIDCount(int maxUIDCount) {
		this.maxUIDCount = maxUIDCount;
	}
	public int getMinUIDCount() {
		return minUIDCount;
	}
	public void setMinUIDCount(int minUIDCount) {
		this.minUIDCount = minUIDCount;
	}
		public int getMaxUID() {
		return maxUID;
	}
	public void setMaxUID(int maxUID) {
		this.maxUID = maxUID;
	}
	public int getMinUID() {
		return minUID;
	}
	public void setMinUID(int minUID) {
		this.minUID = minUID;
	}
	
	
	
	
}
